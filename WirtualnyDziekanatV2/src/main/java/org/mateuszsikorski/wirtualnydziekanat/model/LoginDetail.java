package org.mateuszsikorski.wirtualnydziekanat.model;

public class LoginDetail {

	private String user;
	private String pass;
	
	public LoginDetail() {
	}
	
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	
	
}
